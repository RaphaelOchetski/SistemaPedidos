    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
       
       ProdutoController cc = new ProdutoController();
       
       
        try {
             if(txtDescProduto.getText().equalsIgnoreCase("") || Double.parseDouble(txtPrecoProduto.getText())<= 0.0){
                JOptionPane.showMessageDialog(null, "Valores Invalidos");
             }
             else{
                cc.salvar(txtDescProduto.getText(), Double.parseDouble(txtPrecoProduto.getText()));            
                JOptionPane.showMessageDialog(this, "Produto salvo com sucesso!");
             }
         } catch (SQLException e) {
             JOptionPane.showMessageDialog(this, "Não foi possivel salvar produto!\n" + e.getLocalizedMessage());
         } catch (Exception ex) { 
            Logger.getLogger(ProdutoView.class.getName()).log(Level.SEVERE, null, ex);
        } 

        LimparCampos();
       // }
       
    }//GEN-LAST:event_BtnSalvarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        
        ProdutoController pc = new ProdutoController();
        int produto_id = produto.getIdProduto();
        try {
            
            pc.alterar(produto_id, txtDescProduto.getText(), Double.parseDouble(txtPrecoProduto.getText()));
            
            
            JOptionPane.showMessageDialog(null, "Produto alterado com sucesso!");
          //  LimparCampos();
           
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Não foi possivel alterar o produto!\n" + e.getLocalizedMessage());
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        
        ProdutoController pc = new ProdutoController();
        try {
            pc.excluir(Integer.parseInt(txtCodigoProduto.getText()));
            JOptionPane.showMessageDialog(this, "Produto excluído com sucesso!");
            btnAlterar.setEnabled(false);
            this.setVisible(false);
           
            LimparCampos();           
            

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Não foi possivel excluir o produto!\n" + e.getLocalizedMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

