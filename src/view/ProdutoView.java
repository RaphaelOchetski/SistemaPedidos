  public void recebeTitulo(int produto_id) throws SQLException{
     
     produto = new ProdutoController().buscaProdutoPorId(produto_id);
     
     
     txtCodigoProduto.setText(String.valueOf(produto.getIdProduto()));
     txtDescProduto.setText(produto.getNome());
     txtPrecoProduto.setText(String.valueOf(produto.getPreco()));
     
    
             
     BtnSalvar.setEnabled(false);
     btnAlterar.setEnabled(true);
     btnExcluir.setEnabled(true);
    // comboBoxCandidato.set
      
      
      
  }

