    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
       
        
        TipoProdutoController tp = new TipoProdutoController();
       
         
       try {
            tp.salvar(txtDescTipo.getText());            
            JOptionPane.showMessageDialog(this, "Tipo de Produto salvo com sucesso!");
        
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Não foi possivel salvar tipo!\n" + e.getLocalizedMessage());
        } 
       
       LimparCampos();
      // }
    }//GEN-LAST:event_BtnSalvarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        
        TipoProdutoController tp = new TipoProdutoController();
        int tipo_id = tipoProduto.getIdTipoProduto();
            
            try {
          
            
            tp.alterar(tipo_id, txtDescTipo.getText());
            
            
            JOptionPane.showMessageDialog(null, "Tipo alterado com sucesso!");
          //  LimparCampos();
           
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Não foi possivel alterar o tipo!\n" + e.getLocalizedMessage());
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        
        TipoProdutoController tp = new TipoProdutoController();
        try {
            tp.excluir(Integer.parseInt(txtTipoId.getText()));
            JOptionPane.showMessageDialog(this, "Tipo excluído com sucesso!");
            btnAlterar.setEnabled(false);
            this.setVisible(false);
           
            LimparCampos();           
            

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Não foi possivel excluir o tipo!\n" + e.getLocalizedMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

