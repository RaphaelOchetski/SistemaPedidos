  public void recebeTitulo(int tipo_id) throws SQLException{
     
     tipoProduto = new TipoProdutoController().buscaTipoProdutoPorId(tipo_id);
     
     txtTipoId.setText(String.valueOf(tipoProduto.getIdTipoProduto()));
     txtDescTipo.setText(tipoProduto.getDescricao());
     
      
    
             
     BtnSalvar.setEnabled(false);
     btnAlterar.setEnabled(true);
     btnExcluir.setEnabled(true);
    // comboBoxCandidato.set
      
      
      
  }

